vector<int> addArray(vector<int> a, vector<int> b){
    sort(a.begin(),a.end());
    sort(b.begin(),b.end());

    vector<int> c;
    int lens1 = a.size();
    int lens2 = b.size();

    int r1 = 0, r2 = 0;
    while(r1 < lens1 && r2 < lens2){
        if(a[r1] < b[r2]){
            r1++;
        } else if(a[r1] > b[r2]){
            r2++;
        } else if(a[r1] = b[r2]){
            if(c.empty()){
                c.push_back(a[r1]);
                r1++;
                r2++;
            } else {
                if(c.back() != a[r1]){
                    c.push_back(a[r1]);
                    r1++;
                    r2++;
                } else {
                    r1++;
                    r2++;
                }
            }
        }
    }

    return c;
}

