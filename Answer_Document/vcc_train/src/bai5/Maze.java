		/**
		 * Implement một phương pháp tìm đường nào đó.
		 * <p>
		 * Yêu cầu : Vẽ đường đi từ điểm bắt đầu đến điểm kết thúc. Không hiện
		 * các phần thừa - là các phần đường cụt hoặc đường đi bị sai. Chỉ vẽ
		 * tuyến đường chính đi từ điểm đầu (màu vàng) đến màu đỏ.
		 * <p>
		 * Đường đi từ điểm đầu đến điểm cuối được tô màu xanh dương, để tô màu
		 * xanh dương hãy set giá trị của điểm trên ma trận sang một số > 2
		 */
		public boolean check(int tmp_x, int tmp_y) {
			if((tmp_x < 0) || (tmp_x >= sizeh) || (tmp_y < 0) || (tmp_y >= sizew)) return false;
			if(maze[tmp_x][tmp_y] == 1) return false;
			return true;
		}
		
		public void solution(Map<Node, Node> pre, Node sPoint) {
			Stack<Node> stack = new Stack<>();
			
			while(!pre.isEmpty()) {
				if(sPoint == null) break;
				stack.push(sPoint);
				sPoint = pre.remove(sPoint);
			}
			
			while(!stack.isEmpty()) {
				Node node = stack.pop();
				maze[node.getX()][node.getY()] = 3;
			}
		}
		
		public void solve() {
			// Hàm này chứa phương pháp tìm đường từ điểm start đến vị
			// trí màu đỏ trên ma trận
			int[] hx = {0,0,-1,1};
			int[] hy = {1,-1,0,0};
			Node startPoint = new Node(start, end);
			
			// distance point with start Point
			Map<Node, Integer> dist = new HashMap<>();
			for(int i=0; i<sizeh; i++)
				for(int j=0; j<sizew; j++) {
					Node p = new Node(i,j);
					dist.put(p, 10000); // dist = +vc
				}
			dist.put(startPoint, 0);
			
			Map<Node, Node> pre = new HashMap<>();
			
			LinkedList<Node> queue = new LinkedList<>();
			queue.add(startPoint);
			
			// bfs
			while(!queue.isEmpty()) {
				Node sPoint = queue.remove();

				if(maze[sPoint.getX()][sPoint.getY()] == 2) {
					solution(pre, sPoint);
					return;
				}
				
				for(int i=0; i<4; i++) {
					int tmp_x = sPoint.getX() + hx[i];
					int tmp_y = sPoint.getY() + hy[i];
					
					if(check(tmp_x, tmp_y)) {
						Node p = new Node(tmp_x, tmp_y);
						if(dist.get(p) == 10000) {
							queue.add(p);
							pre.put(p, sPoint); // truoc p la spoint
							dist.put(p, dist.get(sPoint) + 1);
						}
					}
				}
			}
			
		}

